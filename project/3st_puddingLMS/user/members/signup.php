② 회사는 제1항과 같이 회원으로 가입할 것을 신청한 자 중 다음 각 호에 해당하지 않는 한 회원으로 등록합니다. 만약 회사가 회원가입신청의 승인을 거부하거나 유보하는 경우, 회사는 이를 제8조에 따라 회원에게 통지합니다.
1. 가입신청자가 이 약관 제7조 제3항에 의하여 이전에 회원자격을 상실한 적이 있는 경우.
2. 회원의 요청에 의하여 탈퇴한 때로부터 1개월이 지나지 아니한 경우
3. 가입신청자가 기입한 회원정보에 명백한 허위 기재, 누락 또는 오기가 있는 경우
4. 회사가 본인확인 절차를 실시할 경우에 본인 확인이 되지 않거나 본인이 아님이 확인된 경우
5. 가입신청자가 만 14세 미만인 경우
6. 기타 가입신청자를 회원으로 등록하는 것이 사이버몰의 기술상 불가능하거나, 사이버몰의 운영 또는 유지·관리에 현저한 지장을 준다고 판단되는 경우
③ 회원가입계약의 성립시기는 회사의 승낙이 가입신청자에게 도달한 시점으로 합니다.
④ 회원은 회원정보에 변경이 있는 경우, 회원정보 수정 등의 방법으로 그 변경사항을 반영하여야 합니다. 회원정보 수정을 하지 않음으로써 발생하는 회원의 손해에 대하여 회사는 아무런 책임을 지지 않습니다.
⑤ 회사는 『영화 및 비디오물의 진흥에 관한 법률』 및 『청소년 보호법』 및 『화학물질 관리법』등 관련 법령에 따른 등급 및 연령제한 준수를 위하여 회원의 서비스 이용 또는 상품 등 구매를 제한하거나, 회원에게 나이 및 본인확인 등의 절차를 요구할 수 있습니다.

제 7 조 (회원 탈퇴 및 자격 상실 등)
① 회원은 회사에 언제든지 탈퇴를 요청할 수 있으며, 회사는 회원의 회원탈퇴 요청 즉시 회원탈퇴를 처리합니다. 회원탈퇴 시 회원자격이 상실되며, 회사가 제공하는 각종 할인쿠폰, 이벤트 혜택 등이 소멸됩니다. 단, 회원탈퇴 시 소진되지 않은 포인트 등이 있는 경우 이에 대한 권리를 포기한 것으로 간주하여, 탈퇴와 동시에 전부 소멸됩니다. 다만, 회사의 상품대금 후불결제 서비스 이용요금을 결제하지 않은 경우, 탈퇴가 제한됩니다.
② 회원이 다음 각 호의 사유에 해당하는 경우, 회사는 회원의 자격을 제한 또는 상실시킬 수 있습니다.
1. 회원정보에 허위 내용을 입력하거나 타인의 정보를 도용한 경우
2. 사이버몰을 이용하여 구입한 재화 등의 대금, 기타 사이버몰 이용과 관련하여 회원이 부담하는 채무를 기일에 지급하지 않는 경우
3. 다른 사람의 사이버몰 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우
4. 주소, 전자우편 등 회원정보를 잘못 입력하거나 변경하지 않는 등 회원의 귀책사유로 인하여 회사가 상당한 노력을 기울였음에도 불구하고 회원에게 통지 또는 연락을 할 수 없게 된 경우
5. 이 약관 제12조 회원의 의무를 위반하는 행위를 하는 경우
6. 이 약관의 규정 또는 회사의 이용정책 등을 위반하거나 법령 기타 공서양속에 반하는 행위를 하는 경우
③ 회사가 회원 자격을 제한시킨 후 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 회사는 회원 자격을 상실시킬 수 있습니다.
④ 회사가 회원 자격을 상실시키는 경우에는 회원 등록을 말소하며, 이때 회사는 거래 안전을 위하여 해당 회원의 소유로 확인되는 아이디의 회원 등록을 일괄 말소할 수 있습니다. 이 경우 회사는 이를 제8조에 따라 회원에게 통지하고, 회원 등록 말소 전 최소한 30일 이상의 기간을 정하여 소명할 기회를 부여합니다.

제 8 조 (회원에 대한 통지)
① 회사가 회원에 대한 통지를 하는 경우, 회원이 회사와 미리 약정하여 지정한 방법에 따라 전자우편, SMS, 전화, 팩스 등으로 할 수 있습니다.
② 회사는 불특정다수 회원에 대한 통지의 경우 7일 이상 사이버몰의 공지사항(고객센터)에 게시함으로써 개별 통지에 갈음할 수 있습니다. 다만, 회원 본인의 거래에 관련하여 중대한 영향을 미치는 사항에 대하여는 개별 통지합니다.

제 9 조 (정보의 제공 및 광고의 게재)
① 회사는 회원이 서비스 이용 중 필요하다고 인정되는 다양한 정보를 게시판에 공지하거나 전자우편이나 SMS, 전화, 앱푸쉬 등의 방법으로 회원에게 제공할 수 있습니다. 그리고 회원은 법령에 따라 회사에게 부과된 의무이행을 위한 절차안내, 고객문의 등에 대한 답변, 기타 거래관련정보 등을 제외하고는 언제든지 전자우편 등에 대해서 수신 거절을 할 수 있습니다.
② 회사는 서비스의 운영과 관련하여 서비스 화면, 전자우편 등에 광고를 게재할 수 있습니다. 광고가 포함된 전자우편 등을 수신한 회원은 수신거절을 할 수 있으며, 회사는 수신거절을 위한 방법을 회원에게 제공합니다.

제 10 조 (회사의 의무)
① 회사는 이 약관이 금지하거나 관련 법령 기타 공서양속에 반하는 행위를 하지 않으며 이 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하는 데 최선을 다합니다.
② 회사는 회원이 안전하게 서비스를 이용할 수 있도록 회원의 개인정보(신용정보 포함)보호를 위한 보안시스템을 갖추어야 하며, 개인정보처리방침을 공시하고 준수합니다.
③ 회사는 회원이 동의하지 않는 영리목적의 광고성 전자우편, 앱푸쉬 등을 발송하지 않습니다.
④ 회사는 회원으로부터 제기되는 의견이나 불만이 객관적으로 정당하다고 인정될 경우에는 회원의 불만을 해결하기 위해 노력합니다.

제 11 조 (회원의 아이디 및 비밀번호에 대한 의무)
① 아이디와 비밀번호에 관한 관리책임은 회원 본인에게 있습니다.
② 회원은 자신의 아이디 및 비밀번호를 제3자에게 제공, 공개하거나 제3자가 이용하도록 해서는 안 됩니다.
③ 회원이 자신의 아이디 및 비밀번호의 유출 또는 제3자 사용에 대해 인지하고도 비밀번호를 변경하지 않은 경우 또는 이와 같은 사정을 회사에 통지 하지 않거나 회사의 조치에 응하지 않은 경우 등 회원이 본 조의 의무를 준수하지 않아 발생하는 모든 손실이나 손해 등 불이익에 대한 책임은 회원에게 있습니다.

제 12 조 (회원의 의무)
① 회원은 이 약관의 규정, 회사의 이용정책, 이용안내 사항 및 관련 법령 등을 준수하여야 하며, 기타 회사 업무에 방해되는 행위를 하여서는 안 됩니다.
② 회원은 다음 각 호의 행위를 하여서는 안 됩니다.
1. 회원이 회사가 제공하는 서비스를 이용하지 아니하고 판매자와 직접 거래하는 행위(이하 “직거래”) 또는 이를 유도하는 행위
2. 회사 또는 판매자의 판매 활동을 방해하거나 사이버몰을 이용하여 부당한 이득을 취하는 등 통상적인 전자상거래 관행에 부합하지 않는 거래상의 부정 행위
3. 상품의 판매 또는 용역의 제공을 가장한 현금 융통 등 『여신전문금융업법』, 『정보통신망 이용촉진 및 정보보호 등에 관한 법률』 및 기타 관계 법령에 위배되거나 비정상적인 결제 행위
4. 회사가 제공하는 서비스 이용방법에 의하지 아니하고 비정상적인 방법으로 서비스를 이용하거나 회사의 정보처리시스템에 접근하는 행위
5. 회사가 제공하는 서비스와 관련 없거나 비합리적인 요청, 공서양속에 반하는 행위 등으로 회사의 업무를 방해하는 행위
6. 기타 위 각 호에 준하여 회사 또는 제3자의 권리를 침해 또는 침해할 우려가 있거나, 전자상거래 질서를 위협하는 행위
③ 회사는 제2항을 위반한 회원에 대하여 계약 해제, 포인트 등 또는 할인쿠폰의 환수, 회원 자격의 제한, 현금 환불 보류 등의 조치 및 민∙형사 상의 법적 조치 등을 취할 수 있습니다.
④ 본 조 제2항, 제3항의 구체적인 내용은 “푸딩 서비스 이용 정책”에서 정합니다.

제 13 조 (개인정보보호)
① 회사는 회원의 개인정보 수집 시 서비스 제공을 위하여 필요한 범위에서 최소한의 개인정보를 수집합니다.
② 회사는 회원가입 시 구매계약이행에 필요한 정보를 미리 수집하지 않습니다. 다만, 관련 법령상 의무이행을 위하여 구매계약 이전에 본인확인이 필요한 경우로서 최소한의 특정 개인정보를 수집하는 경우에는 그러하지 아니합니다.
③ 회사는 회원의 개인정보를 수집·이용하는 때에는 당해 회원에게 그 목적을 고지하고 동의를 받습니다.
④ 회사는 수집된 개인정보를 목적 외의 용도로 이용할 수 없으며, 새로운 이용목적이 발생한 경우 또는 제3자에게 제공하는 경우에는 이용·제공단계에서 당해 회원에게 그 목적을 고지하고 동의를 받습니다. 다만, 관련 법령에 달리 정함이 있는 경우에는 예외로 합니다.
⑤ 회사가 제3항과 제4항에 의해 회원의 동의를 받아야 하는 경우에는 개인정보 관리 책임자의 신원(소속, 성명 및 전화번호, 기타 연락처), 정보의 수집목적 및 이용목적, 제3자에 대한 정보제공 관련사항(제공받은 자, 제공목적 및 제공할 정보의 내용) 등 『정보통신망 이용촉진 및 정보보호 등에 관한 법률』 에서 규정한 사항을 미리 명시하거나 고지해야 하며 회원은 언제든지 이 동의를 철회할 수 있습니다.
⑥ 회원은 언제든지 회사가 가지고 있는 자신의 개인정보에 대해 열람 및 오류 정정을 요구할 수 있으며 회사는 이에 대해 지체 없이 필요한 조치를 취할 의무를 집니다. 회원이 오류의 정정을 요구한 경우에는 회사는 그 오류를 정정할 때까지 당해 개인정보를 이용하지 않습니다.
⑦ 회사는 개인정보 보호를 위하여 회원의 개인정보를 취급하는 자를 최소한으로 제한하여야 하며 신용카드, 은행계좌 등을 포함한 회원의 개인정보의 분실, 도난, 유출, 동의 없는 제3자 제공, 변조 등으로 인한 회원의 손해에 대하여 모든 책임을 집니다.
⑧ 회사 또는 그로부터 개인정보를 제공받은 제3자는 개인정보의 수집목적 또는 제공받은 목적을 달성한 때에는 당해 개인정보를 지체 없이 파기합니다.
⑨ 회사는 개인정보의 수집·이용·제공에 관한 동의란을 미리 선택한 것으로 설정해두지 않습니다. 또한 개인정보의 수집·이용·제공에 관한 동의 거절 시 제한되는 서비스를 구체적으로 명시하고, 필수수집항목이 아닌 개인정보의 수집·이용·제공에 관한 회원의 동의 거절을 이유로 회원가입 등 서비스 제공을 제한하거나 거절하지 않습니다.
⑩ 회사의 사이버몰(제2조 제1호에서 정한 범위 내) 이외의 링크된 사이버몰에서는 회사의 개인정보처리방침이 적용되지 않습니다. 링크된 사이버몰 및 상품 등을 제공하는 제3자의 개인정보 취급과 관련하여서는 해당 사이버몰 및 제3자의 개인정보처리방침을 확인할 책임이 회원에게 있으며, 회사는 이에 대하여 책임을 부담하지 않습니다.
⑪ 회사는 다음과 같은 경우에 법이 허용하는 범위 내에서 회원의 개인정보를 제3자에게 제공할 수 있습니다.
1. 수사기관이나 기타 정부기관으로부터 적법한 절차에 따른 정보제공을 요청 받은 경우
2. 회원이 관련 법령을 위반하는 등의 부정행위를 확인하기 위해 필요한 경우
3. 판매자 또는 배송업체 등에게 거래 및 배송 등에 필요한 최소한의 회원의 정보(성명, 주소, 전화번호)를 제공하는 경우
4. 구매가 성사된 때에 그 이행을 위하여 필요한 경우와 구매가 종료된 이후에도 반품, 교환, 환불, 취소 등을 위하여 필요한 경우
5. 기타 관련 법령에 의해 요구되는 경우
⑫ 기타 본 조에서 정하지 않은 사항은 회사가 마련한 “개인정보 처리방침”에 따릅니다.

제 14 조 (지급방법)
① 사이버몰에서 구매한 상품 등에 대한 대금지급 방법은 다음 각 호의 방법 중 가용한 방법으로 할 수 있습니다.
1. 폰뱅킹, 인터넷뱅킹 등의 각종 계좌이체
2. 선불카드, 직불카드, 신용카드 등의 각종 카드 결제
3. 온라인 무통장입금
4. 전자화폐에 의한 결제
5. 수령 시 대금지급
6. 포인트 등에 의한 결제
7. 회사와 계약을 맺었거나 회사가 인정한 상품권에 의한 결제
8. 기타 전자적 지급 방법에 의한 대금 지급 등
② 회원이 구매 대금의 결제와 관련하여 입력한 정보 및 그 정보와 관련하여 발생한 책임과 불이익은 전적으로 회원이 부담합니다.
③ 회사는 상품 등의 대금에 회원의 지급방법에 대한 수수료를 추가하여 징수할 수 없습니다. 다만, 회사는 상품대금 후불 결제서비스 이용시 고지된 연체료 등 지연손해금을 회원에게 청구할 수 있습니다.

제 15 조 (할인쿠폰)
① 할인쿠폰은 회사가 무상으로 발행하는 쿠폰으로 발행대상, 발행경로, 사용대상 등에 따라 구분될 수 있으며, 할인쿠폰의 세부구분, 할인금액(할인율), 사용방법, 사용기한 및 제한에 대한 사항은 할인쿠폰 또는 서비스 화면에 표시됩니다. 할인쿠폰의 종류 및 내용과 발급 여부는 회사의 서비스 정책에 따라 변경될 수 있습니다.
② 할인쿠폰은 현금으로 출금될 수 없으며, 할인쿠폰에 표시된 유효기간이 만료되거나 회원자격이 상실되면 소멸합니다.
③ 할인쿠폰은 회사에서 별도로 명시한 경우를 제외하고는 타인에게 양도할 수 없으며, 부정한 목적이나 용도로 사용할 수 없습니다. 이를 위반한 경우 회사는 할인쿠폰을 소멸시키거나 회원자격을 제한 또는 상실시킬 수 있습니다.
④ 회원이 부정한 방법으로 포인트 등 또는 할인쿠폰을 획득한 사실이 확인될 경우, 회사는 이를 회수하고, 회원자격 상실(아이디 삭제) 및 민∙형사 상의 법적 조치 등을 취할 수 있습니다.

제 16 조 (티켓서비스)
① 티켓서비스란 회원이 일정한 시설을 이용하거나 용역을 제공받을 수 있는 권리를 상품으로 제공·판매하는 것을 말합니다.
② 회사는 회원이 티켓서비스를 이용할 수 있도록 티켓서비스 제공업체가 발행한 증거증권을 회원에게 판매합니다.
③ 회원은 티켓서비스 제공업체가 지정한 방법으로 본인확인 절차를 거쳐(단, 구매 확인 목적으로 전달되는 전자우편, 문자메시지, 기타 영수증으로는 본인확인을 대신할 수 없습니다), 개별조건 상에 명시된 유효기간 내에서만 티켓서비스를 이용할 수 있으며, 이를 타인에게 양도할 수 없습니다.
④ 미사용티켓 환불제 적용 상품의 경우, 회원은 유효기간 내에 이용하지 못한 티켓서비스의 구매 대금을 회사의 정책에 따라 포인트 등으로 환급 받을 수 있습니다.
⑤ 회원은 사이버몰에 명시된 내용 및 개별 주의사항(사용조건)을 확인하여야 할 책임이 있으며, 명시된 사용조건에 따라서만 티켓서비스를 이용하실 수 있습니다.

제 17 조 (모바일 상품권 등의 이용)
① 회사는 제15조 및 제16조 등과 구별하여, 상품권을 구매한 회원이 언제든지 타인에게 양도할 수 있고, 소지만 하면 사용에 제한이 없는 전자적 형태의 온라인·모바일 상품권 등(이하 ‘모바일 상품권’이라 합니다)을 발행자로부터 제공 받아 회원에게 판매할 수 있습니다. 회원은 본 조의 모바일 상품권을 구매하였을 경우, 모바일 상품권에서 지정하는 바에 따라 발행자 또는 발행자가 지정한 다른 업체에서 이를 사용할 수 있습니다.
② 회원은 회사의 사이버몰에 명시된 내용 및 모바일 상품권에 기재된 주의사항을 확인하여야 할 책임이 있으며, 명시된 이용 조건에 따라 상품권을 사용할 수 있습니다.
③ 본 조에 따라 판매되는 모바일 상품권에 대해서는 이 약관에도 불구하고, 다음 각 호의 내용이 우선하는 것으로 합니다.
1. 회원이 모바일 상품권을 사용할 수 있는 유효기간은 공정거래위원회의 ‘신유형 상품권 표준약관(표준약관 제10073호, 이 약관 시행 이후 개정될 경우 그 개정약관을 가리키며, 이하 “상품권 표준약관”) 제5조에 따르며, 회원은 1회(3개월)이상 유효기간 연장을 발행자에게 요청할 수 있습니다.
2. 회원은 상품권 표준약관 제7조에 따라 모바일 상품권을 환불 받거나 일부 잔액을 반환 받을 수 있습니다.
3. 모바일 상품권의 표시사항, 지급보증 또는 피해보상보험계약 등 그 밖의 모바일 상품권에 관한 내용은 상품권 표준약관의 각 해당 내용에 따르며, 회원은 위 표준약관의 내용을 이 약관의 일부로 주장할 수 있습니다.

제 18 조 (포인트 등)
① 회원은 사이버몰 혹은 회사의 제휴사에서 상품 등을 구매할 때 포인트 등을 결제 수단으로 사용할 수 있습니다.
② 회원이 포인트 사용하기 위해서는 포인트 서비스 이용약관』, 포인트 전자금융거래이용약관』에 동의하여야 합니다.
③ 이벤트 또는 회사의 서비스 정책, 마케팅 및 프로모션을 통하여 지급되는 무상의 포인트 등의 유효기간은 각각 별도로 설정될 수 있으며, 유상의 포인트 등과 달리 환급의 대상이 되지 않습니다.
④ 회원은 무상의 포인트 등을 그 유효기간 이내에 사용하여야 하고, 유효기간 이내에 사용하지 않을 경우 소멸됩니다. 회사는 이벤트 등을 통해 무상으로 적립된 포인트 등의 적립 사유가 부존재하는 것으로 밝혀지거나 사후적으로 소멸한 경우 또는 회원 자격의 상실 시에는 이를 환수할 수 있습니다.
⑤ 포인트 등을 사용 시, 유효기간 만료일이 먼저 도래하는 포인트 등을 우선하여 사용됩니다.


